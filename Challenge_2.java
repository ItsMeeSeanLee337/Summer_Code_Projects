import java.util.Scanner;
class Main 
{
  public static void main (String[] args) 
  {
    System.out.println("Please enter Semester 1 Grade");
    Scanner grade = new Scanner (System.in);
      double grades1 = grade.nextDouble();
    System.out.println("Please enter Semester 2 Grade");
      double grades2 = grade.nextDouble();
    System.out.println("Please enter Midterm Grade");
      double gradem = grade.nextDouble();
    System.out.println("Please enter Final Grade");
      double gradesf = grade.nextDouble();
    double fgrade = ((grades1 * 0.35) + (grades2 * 0.35) + (gradem * 0.15) + (gradesf * 0.15));
    System.out.println("Your Yearly grade is " + fgrade);
    if (fgrade >= 90)
    {
      System.out.println("Your Year grade is an A");
    }    
    else if (fgrade >= 80 )
    {
      System.out.println("Your Year grade is an B");
    }
    else if (fgrade >= 70)
    {
      System.out.println("Your Year grade is an C");
    }
    else if (fgrade >= 64)
    {
      System.out.println("Your Year grade is an D");
    }
    else
    {
      System.out.println("Your Year grade is an F");
    }
  }
}
